let compras=['banana', 'uva', 'laranja'];

console.log('O join coloca o que você pedir entre cada inserção do array:')
console.log(compras.join('-'));
console.log ('O pop exclui o ultimo item:')
console.log(compras.pop());
console.log ('O push coloca essa nova variavel no array:')
console.log(compras.push('ovo'));
console.log ('O shift remove o primeiro elemento da lista e diz qual foi')
console.log(compras.shift());
console.log('O unshift acrescenta um novo elemento colocado na string:')
console.log(compras.unshift('pera'));
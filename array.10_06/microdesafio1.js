let compras = ['arroz', 'feijao', 'batata', 'macarrrao']
compras.join ('')/* juntas os eementos  */
console.log("Reultado do join: " + "junta os elementos")

compras.pop ('')/* elimina o ultimo */
console.log("Reultado do .pop: " + "elimina o ultimo elemento da array")

compras.push ('') /* adinciona mais elementos ao final */
console.log("Reultado do .push: " + "adiciona mais um elemento ao final da array")

compras.shift ('') /* elimina o primeiro */
console.log("Reultado do .shift: " + "elimina o primeiro elemento")

compras.unshift ('') /*  adiciona elemtno no inicio */
